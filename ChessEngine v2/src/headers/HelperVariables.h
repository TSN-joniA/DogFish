// convert ASCII character pieces to encoded constants
inline int convertToCharPiece(char pieceChar) {
    switch (pieceChar) {
    case 'P': return P;
    case 'N': return N;
    case 'B': return B;
    case 'R': return R;
    case 'Q': return Q;
    case 'K': return K;
    case 'p': return p;
    case 'n': return n;
    case 'b': return b;
    case 'r': return r;
    case 'q': return q;
    case 'k': return k;
    default: return -1;
    }
}